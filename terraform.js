function generateOutputs(nodes, cloud) {
  if (nodes.length === 0) return `# Outputs will be generated based on your architecture`;
  
  let code = `# Outputs\n\n`;
  nodes.slice(0, 6).forEach(node => {
    const resName = sanitizeName(node.label);
    const tfType = node.service.tf;
    code += `output "${resName}_id" {\n  description = "ID of ${node.label}"\n  value       = ${tfType}.${resName}.id\n}\n\n`;
  });
  return code;
}

function sanitizeName(str) {
  return str.toLowerCase().replace(/[^a-z0-9_]/g, '_').replace(/^_+|_+$/g, '') || 'resource';
}

function initTerraform() {
  const modal = document.getElementById('terraform-modal');
  let currentTab = 'main';
  let currentTf = {};
  
  document.getElementById('btn-terraform').addEventListener('click', () => {
    currentTf = generateTerraform(nodes, nodes[0]?.cloud || 'aws');
    showTfContent(currentTf.main);
    modal.classList.remove('hidden');
  });
  
  document.getElementById('close-terraform').addEventListener('click', () => modal.classList.add('hidden'));
  modal.addEventListener('click', (e) => { if (e.target === modal) modal.classList.add('hidden'); });
  
  document.querySelectorAll('.modal-tab').forEach(tab => {
    tab.addEventListener('click', () => {
      currentTab = tab.dataset.tab;
      document.querySelectorAll('.modal-tab').forEach(t => t.classList.remove('active'));
      tab.classList.add('active');
      showTfContent(currentTf[currentTab] || '');
    });
  });
  
  document.getElementById('btn-copy-tf').addEventListener('click', () => {
    const content = document.getElementById('tf-content').textContent;
    navigator.clipboard.writeText(content).then(() => showToast('Copied to clipboard!'));
  });
  
  document.getElementById('btn-download-tf').addEventListener('click', () => {
    const content = document.getElementById('tf-content').textContent;
    const blob = new Blob([content], { type: 'text/plain' });
    const a = document.createElement('a'); a.href = URL.createObjectURL(blob);
    a.download = currentTab + '.tf'; a.click();
    showToast('Downloaded ' + currentTab + '.tf');
  });
}

function showTfContent(code) {
  const el = document.getElementById('tf-content');
  el.innerHTML = syntaxHighlight(code);
}

function syntaxHighlight(code) {
  return code
    .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
    .replace(/(#[^\n]*)/g, '<span class="tf-comment">$1</span>')
    .replace(/\b(resource|variable|output|provider|terraform|required_providers|required_version|source|version)\b/g, '<span class="tf-keyword">$1</span>')
    .replace(/"([^"]+)"/g, (m, p) => {
      if (p.includes('\n')) return m;
      return `<span class="tf-string">"${p}"</span>`;
    })
    .replace(/\b(true|false|null)\b/g, '<span class="tf-value">$1</span>');
}
