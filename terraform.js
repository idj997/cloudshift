// Terraform code generation
function generateTerraform(targetNodes, targetCloud) {
  const cloud = targetCloud || (targetNodes.length > 0 ? targetNodes[0].cloud : 'aws');
  
  const main = generateMain(targetNodes, cloud);
  const variables = generateVariables(cloud);
  const outputs = generateOutputs(targetNodes, cloud);
  
  return { main, variables, outputs };
}

function generateMain(nodes, cloud) {
  if (nodes.length === 0) {
    return `# No resources added yet.\n# Drag services onto the canvas to generate Terraform code.\n\n# Provider configuration\nterraform {\n  required_version = ">= 1.5.0"\n  required_providers {\n    ${getProviderBlock(cloud)}\n  }\n}\n\nprovider "${getProviderName(cloud)}" {\n  ${getProviderConfig(cloud)}\n}`;
  }
  
  let code = `# Generated by CloudShift\n# Architecture: ${nodes.length} resource${nodes.length !== 1 ? 's' : ''}\n# Cloud: ${cloud.toUpperCase()}\n\n`;
  code += `terraform {\n  required_version = ">= 1.5.0"\n  required_providers {\n    ${getProviderBlock(cloud)}\n  }\n}\n\n`;
  code += `provider "${getProviderName(cloud)}" {\n  ${getProviderConfig(cloud)}\n}\n\n`;
  
  // Group by type for better organization
  const byCategory = {};
  nodes.forEach(n => {
    const cat = n.service.category || 'other';
    if (!byCategory[cat]) byCategory[cat] = [];
    byCategory[cat].push(n);
  });
  
  for (const cat in byCategory) {
    code += `# ── ${cat.toUpperCase()} ────────────────────────────\n\n`;
    byCategory[cat].forEach(node => {
      code += generateResource(node, cloud);
      code += '\n';
    });
  }
  
  return code;
}

function generateResource(node, cloud) {
  const resName = sanitizeName(node.label);
  const tfType = node.service.tf;
  
  let attrs = getDefaultAttributes(node.service.id, cloud, resName);
  
  return `resource "${tfType}" "${resName}" {\n${attrs}\n}\n`;
}

